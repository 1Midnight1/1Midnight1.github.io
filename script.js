let elementsArray = document.querySelectorAll(".project, .section-header");
window.addEventListener('scroll', fadeIn ); 
function fadeIn() {
    for (var i = 0; i < elementsArray.length; i++) {
        var elem = elementsArray[i]
        var distInView = (elem.getBoundingClientRect().top + elem.getBoundingClientRect().bottom)/2 - window.innerHeight + 20;
        if (distInView < 0) {
            elem.classList.add("inView");
        } else {
            elem.classList.remove("inView");
        }
    }
}
fadeIn()